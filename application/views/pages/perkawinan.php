<h2>Persyartan</h2>
<p>Untuk mendapatkan Pelayanan Pencatatan Perkawinan, harus melengkapi persyaratan berikut ini:<br>
a. Surat Keterangan dari Lurah sesuai domisili yang bersangkutan.<br>
b.Surat pemberkatan perkawinan dari pemuka agama atau surat perkawinan Penghayat Kepercayaan yang ditandatangani oleh Pemuka Penghayat  Kepercayaan bagi yang terlambat pelaporannya lebih dari 60 (enam puluh) hari sejak terjadinya perkawinan.<br>
c. KK dan KTP suami dan istri.<br>
d. Foto berwarna suami dan istri berdampingan ukuran 4 x 6 cm sebanyak 5 (lima) lembar.<br>
e. Kutipan Akta Kelahiran suami dan istri.<br>
f. Kutipan Akta Perceraian atau Kutipan Akta Kematian suami/istri bagi mereka yang pernah kawin.<br>
g. Pencatatan perkawinan yang tidak memiliki bukti perkawinan dikarenakan perkawinan adat maka pembuktian perkawinannya harus melalui proses Penetapan Pengadilan Negeri.<br>
h. Legalisasi dari pemuka agama/pendeta/penghayat kepercayaan di tempat terjadinya perkawinan bagi pencatatan perkawinan yang melampaui batas waktu.<br>
i. Dua orang saksi yang memenuhi syarat.<br>
j. Bagi mempelai yang berusia di bawah 21 (dua puluh satu) tahun harus ada izin dari orang tua.<br>
k. Surat Izin Pengadilan Negeri bagi calon mempelai di bawah usia 21 (dua puluh satu) tahun, apabila tidak mendapat persetujuan dari orang tua.<br>
I. Surat izin Pengadilan Negeri apabila calon mempelai pria di bawah usia 19 (sembilan belas) tahun dan wanita di bawah usia 16 (enam belas) tahun.<br>
m. Surat Keputusan Pengadilan Negeri yang telah mempunyai kekuatan hukum yang tetap bila ada sanggahan.<br>
n. Dispensasi Camat apabiia pelaksanaan pencatatan perkawinan kurang dari sepuluh hari sejak tanggal pengajuan permohonan.<br>
o. Kutipan Akta Kelahiran Anak yang akan disahkan dalam perkawinan, apabila ada.<br>
p. Pengumuman perkawinan.<br>
q. Akta Perjanjian Perkawinan dari Notaris yang disahkan pada saat pencatatan perkawinan oleh pegawai pencatat pada Dinas dan Suku Dinas.<br>
r.  Surat Izin dari Komandan bagi anggota TNI dan POLRI , dan<br>
s. Bagi Orang Asing melampirkan dokumen :<br>
1. Paspor.<br>
2. KITAP/KITAS Dokumen dari imigrasi;<br>
3. SKLD Dokumen dari kepolisian;<br>
4. KTP/KKISKTI/SKDS Dokumen pendaftaran Orang Asing dari Dinas, dan<br>
5. Surat Izin dari Kedutaan/Perwakilan dari Negara Asing.<br>

<h2>Waktu yang tepat untuk mencatatkan perkawinan</h2>
1. 10 (sepuluh) hari kerja sejak tanggal Pendaftaran. Jika kurang dari 10 (sepuluh) hari kerja, harus dengan Dispensasi dari Camat yang harus ditandatangani Camat<br>
2. Pencatatkan perkawinan Anda sebelum 1 (satu) bulan sejak Perkawinan menurut Agama dilangsungkan<br><br>

