		<p>Dinas Kependudukan dan Catatan Sipil Propinsi DKI Jakarta melayani Pencatatan Perkawinan bagi mereka yang telah melangsungkan perkawinan menurut hukum dan tata cara agama selain agama Islam. Pencatatan Perkawinan didasarkan atas Undang-undang Nomor 1 Tahun 1974 tentang Perkawinan. <br><br>

Sesuai dengan Undang-Undang No. 24 Tahun 2013 tentang Perubahan atas Undang-Undang No. 23 Tahun 2006 Pencatatan perkawinan yang sebelumnya berdasarkan atas asa peristiwa, sejak ditetapkannya undang-undang ini berubah menjadi berdasarkan atas domisli. Sehingga pencatatan dilakukan pada instansi pelaksanan sesuai dengan domisili pelapor. <br><br>

Sesuai dengan Undang-undang Nomor 1 Tahun 1974, usia perkawinan adalah 19 tahun bagi Pria dan 16 tahun bagi Wanita. <br><br>

Jika Anda melangsungkan perkawinan dalam usia di bawah 21 tahun, maka Anda harus memperoleh ijin dari orang tua. Apabila Anda masih di bawah 19 tahun bagi Pria dan di bawah usia 16 tahun bagi Wanita, maka harus memperoleh Dispensasi dari Pengadilan Negeri. <br><br>

Berdasarkan Undang-Undang No. 24 tahun 2013 tentang Perubahan atas Undang-Undang No. 23 Tahun 2006 tentang Administrasi Kependudukan bahwa Pencatatan kelahiran wajib dilaporkan oleh penduduk pada Instansi pelaksana tempat terjadinya peristiwa diubah menjadi wajib dilaporkan oleh penduduk pada Instansi pelaksana tempat penduduk berdomisili.<br></p>

