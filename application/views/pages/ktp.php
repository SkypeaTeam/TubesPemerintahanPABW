Pembuatan KTP dilakukan selambat-lambatnya 14 hari sejak :<br>
 
a. Tanggal berusia 17 (tujuh belas) tahun; atau<br>
b. Tanggal perkawinan jika kawin di bawah usia 17 (lujuh belas) tahun;<br>
c. Tanggal diterbilkannya Sural Keterangan Pindah Datang bagi penduduk yang datang dari luar daerah atau luar negeri; dan<br>
d. Tanggal pelaporan perubahan status kependudukan dari penduduk sementara Orang Asing menjadi penduduk.<br><br>
 
Penggantian KTP dilakukan selambat-lambatnya 14 hari sejak berakhir masa berlakunya KTP.<br><br>
 
<h2>Persyaratan Pembuatan KTP Baru</h2>
 
Untuk memperoleh Kartu Tanda Penduduk (KTP) baru harus melengkapi syarat-syarat berikut :<br>
 
a. Surat Pengantar RT/RW;<br><br>
 
b. Asli dan Fotokopi :<br>
1. KK<br>
2. Kutipan Akta Kelahiran;<br>
3. Kutipan Akta Nikah/Akta Kawin bagi penduduk yang belum berusia 17 (tujuh belas) tahun;<br>
4. Paspor; dan<br>
5. Izin Tinggal Tetap.<br><br>
 
c. Bukti pembayaran keterlambatan pembuatan KTP.<br><br>
 
 
<h2>Persyaratan Perpanjangan KTP</h2>
 
Untuk memperpanjang Kartu Tanda Penduduk yang sudah habis masa berlakunya harus melengkapi syarat-syarat berikut :<br>
 
1. Surat Pengantar dari RT/RW<br>
2. KTP lama yang sudah habis masa berlakunya<br>
3. Fotocopy Kartu Keluarga<br>
4. Foto Langsung<br>
5. Surat Keterangan lapor kehilangan KTP dari Kepolisian bagi yang kehilangan KTP<br>
6. Bukti Pembayaran Keterlambatan Perpanjangan KTP<br><br>
 
 
<h2>Masa Berlaku KTP</h2>
 
KTP berlaku untuk jangka waktu 5 tahun, kecuali manula (berusia di atas 60 tahun), KTP berlaku seumur hidup. <br><br>
 
<h2>Prosedur Pelayanan</h2>
Tugas Kewajiban Penduduk : Datang ke kantor Kelurahan dengan membawa :<br>
 
1. KTP lama<br>
2. Foto copy Kartu Keluarga dan aslinya<br>
3. Foto Langsung<br>
4. Surat Pengantar dari RT / RW<br>
5. Surat Kuasa bagi penduduk yang tidak bisa mengambil sendiri dengan diketahui RT / RW<br><br>
 
<h2>Tugas dan kewajiban Kepala Kelurahan : Bila data penduduk sudah benar :</h2>
 
1. Menerima dan meneliti seluruh berkas persyaratan<br>
2. Mencocokkan KTP lama warga dengan KTP baru<br>
3. Menandatangani KTP dan menerima retribusinya<br>
4. Menyelesaikan proses administrasi lainnya lebih lanjut
Namun apabila datanya salah, KTP yang mengalami perubahan data agar dibuatkan Surat Mutasi Rubah.<br><br>
 
