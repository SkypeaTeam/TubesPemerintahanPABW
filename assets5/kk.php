Kartu keluarga dicetak rangkap 4 yang masing-masing dipegang oleh :<br>
a. Kepala Keluarga (Iembar pertama);<br>
b. Ketua Rukun Tetangga (Iembar kedua).<br>
c. Lurah (Iembar ketiga) ; dan<br>
d. Suku Dinas (Iembar keempat).<br><br>
 
<h2>Perubahan Data</h2>
 
Setiap terjadi perubahan data dalam Kartu Keluarga seperti karena terjadi peristiwa Kelahiran, Kematian, Kepindahan, dll, Kepala Keluarga wajib melaporkan ke kelurahan dengan persyaratan<br>
a. Surat Pengantar RT/RW<br>
b. KK lama<br>
c. Asli dan Fotokopi Surat Keterangan Kematian/Kutipan Akta Kematian;<br>
d. Asli dan Fotokopi Akta Perkawinan/Perceraian bagi yang pernah menikah/ bercerai<br>
e. Surat Keterangan Pindah bagi penduduk yang pindah, dan<br>
f. Asli dan Fotokopi Kutipan Akta Perceraian, bagi pengurangan anggota keluarga karena cerai.<br>
Dari hasil perlaporan tersebut akan diterbitkan Kartu Keluarga baru.<br><br>
 
<h2>Kepindahan</h2>
 
Apabila suatu keluarga pindah seluruhnya ke tempat lain, maka Kartu Keluarga yang disimpan di Kepala Keluarga dan di Ketua RT harus diserahkan kepada Lurah (dicabut). Di tempat tinggal yang baru, berdasarkan Surat Keterangan Pindah, Lurah akan memberi Kartu Keluarga yang baru.<br><br>
 
<h2>Persyaratan Pembuatan KK</h2>
 
Untuk membuat Kartu Keluarga harus melengkapi syarat-syarat berikut: <br>a. Surat Pengantar RT/RW<br>
b. Biodata penduduk<br>
c. KK lama<br>
d. Asli dan Fotokopi Kutipan Akta Perkawinan/Akta Nikah<br>
e. Asli dan Fotokopi Izin Tinggal Tetap bagi Orang Asing<br>
f. Surat Keterangan Pindah/Surat Keterangan Pindah Datang (SKP/SKPD) dan<br>
g. Surat Keterangan Datang dari Luar Negeri (SKDLN) bagi WNI yang datang dari luar negeri.<br><br>
 
<h2>Perhatian</h2>
 
Kartu Keluarga (KK) adalah Dokumen milik Pemda Propinsi DKI Jakarta dan karena itu tidak boleh mencoret, merubah, mengganti, menambah isi data yang tercantum dalam Kartu Keluarga.
Setiap terjadi perubahan karena Mutasi Data dan Mutasi Biodata, wajib dilaporkan kepada Lurah dan akan diterbitkan Kartu Keluarga (KK) yang baru<br>
Pendatang baru yang belum mendaftarkan diri atau belum berstatus penduduk DKI Jakarta, nama dan identitasnya tidak boleh dicantumkan dalan Kartu Keluarga.

